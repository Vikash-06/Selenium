package javabasic;

public class LoopsConcept {

	public static void main(String[] args) {
		//Task is to print 1 to 10 number
		//1. while loop
		 int i =1;
		 while (i<=10) {
			 System.out.println(i);
			 i++;
		 }
	
		 //2. for loop
		 
		 for (int k = 1; k<=10; k++) {
			 System.out.println(k);
		 }
		 //task is to print 10 to 1 number
		 
		 for(int l=10; l>=1; l--) {
			 System.out.println(l);
		 }

	}

}
