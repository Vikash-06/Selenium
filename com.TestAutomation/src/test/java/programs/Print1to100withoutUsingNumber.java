package programs;

public class Print1to100withoutUsingNumber {

	public static void main(String[] args) {
		// print 1 to 100 numbers without using number

		int one = 'a' / 'a'; // 1
		String s1 = "..........";
		for (int i = one; i <= (s1.length() * s1.length()); i++) {
			System.out.println(i);
		}

	}
}
